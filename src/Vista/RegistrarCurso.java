    private void limpiar() {
        this.txtCodCurso.setText("");
        this.txtNombre.setText("");
        this.spnHorasPracticas.setValue(0);
        this.spnHorasTeoricas.setValue(0);
        this.cbxDocente.setSelectedIndex(0);
    }

    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        try {
            String codigo = this.txtCodCurso.getText();
            String nombre = this.txtNombre.getText();
            int horasT = Integer.parseInt(this.spnHorasTeoricas.getValue().toString());
            int horasP = Integer.parseInt(this.spnHorasPracticas.getValue().toString());
            Docente docente = (Docente)this.modeloComboCurso.getElementAt(this.cbxDocente.getSelectedIndex());
            
            Curso objCurso = new Curso(codigo, nombre, horasT, horasP, docente);

            boolean resultado = this.objRegistroCurso.agregar(objCurso);
            
            if (resultado) {
                JOptionPane.showMessageDialog(this, CURSO  +" registrado con éxito!");
            } else {
                JOptionPane.showMessageDialog(this, "Error en el registro!");
            }
            this.limpiar();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error en ingreso de datos de " + CURSO, "Datos ingresados en el formulario no son correctos.", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnRegistrarActionPerformed

