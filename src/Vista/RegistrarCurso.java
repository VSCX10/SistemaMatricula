    private void llenarDocentesEnCombo(){
        Persona [] listaDocentes = objRegistroPersona.getListaDocente();
        for (int i = 0; i < listaDocentes.length; i++) {
            Docente oDocente = (Docente)listaDocentes[i];
            this.modeloComboCurso.addElement(oDocente);
        }
    }

