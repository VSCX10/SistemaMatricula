    public void limpiarTabla(){
        model = new DefaultTableModel();
        this.tblCursos.setModel(model);
        
        
        for (String column : COLUMNS) {
           model.addColumn(column); 
        }
    }
    
    public void moverArregloATabla(){
        this.limpiarTabla();
        Curso [] lista =  this.objRegistroCurso.getListaCursos();
        String [] fila = new String[COLUMNS.length];
        
        for (int i = 0; i < lista.length; i++) {
            Curso objCurso = (Curso)lista[i];
            
            //Por cada persona, agrego a la fila
            fila[0] = String.valueOf(objCurso.getCodCurso());
            fila[1] = objCurso.getNombreCurso();
            fila[2] = String.valueOf(objCurso.getHorasT());
            fila[3] = String.valueOf(objCurso.getHorasP());
            
            Docente docente = objCurso.getDocente();
            fila[4] = docente.getNombre() + " " + docente.getApellido();
            
            model.addRow(fila);
        }
        
        this.lblTotal.setText(String.valueOf(lista.length));
    }

